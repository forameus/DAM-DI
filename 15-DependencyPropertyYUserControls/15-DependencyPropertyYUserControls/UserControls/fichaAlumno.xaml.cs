namespace _15_DependencyPropertyYUserControls.UserControls
{
    public sealed partial class fichaAlumno : UserControl
    {
        public fichaAlumno()
        {
            this.InitializeComponent();
        }




        public int Username
        {
            get { return (int)GetValue(UsernameProperty); }
            set { SetValue(UsernameProperty, value); }
        }

        // Using a DependencyProperty as the backing store for Username.  This enables animation, styling, binding, etc...
        public static readonly DependencyProperty UsernameProperty =
            DependencyProperty.Register("Username", typeof(int), typeof(ownerclass), new PropertyMetadata(0));




        public int Password
        {
            get { return (int)GetValue(MyPropertyProperty); }
            set { SetValue(MyPropertyProperty, value); }
        }

        // Using a DependencyProperty as the backing store for MyProperty.  This enables animation, styling, binding, etc...
        public static readonly DependencyProperty MyPropertyProperty =
            DependencyProperty.Register("MyProperty", typeof(int), typeof(ownerclass), new PropertyMetadata(0));



    }
}
